inline bool getMapZoneFromID(MapZoneID id, MapZoneData& zone)
{
    if(MapZones.find(id) == MapZones.end())
        return false;

    zone = MapZones.at(id);

    return true;
}

inline bool getMapZoneFromPosition(const Vector3& position, MapZoneData& mapZone)
{
    for(auto const &zone : MapZones)
    {
        const Vector3& min = zone.second.minXYZ;
        const Vector3& max = zone.second.maxXYZ;

        if (
            (min.x <= position.x && position.x <= max.x) &&
            (min.y <= position.y && position.y <= max.y) &&
            (min.z <= position.z && position.z <= max.z)
        ) {
            mapZone = zone.second;
            return true;
        }
    }

    return false;
}